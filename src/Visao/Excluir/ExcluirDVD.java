    private void comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboActionPerformed
       Connection con = Conexao.AbrirConexao();
        DVDDAO sql = new DVDDAO(con);
        List<DVD> lista = new ArrayList<>();
        String nome = combo.getSelectedItem().toString();
        int c = Integer.parseInt(nome);
        lista = sql.Listar_Codigo_DVD(c);

        for(DVD b : lista){
            int a = b.getCodigo();
            codi.setText(""+ a);
        }
        Conexao.FecharConexao(con);
    }//GEN-LAST:event_comboActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String cod = codi.getText();
        String nome = combo.getSelectedItem().toString();

        Connection con = Conexao.AbrirConexao();
        DVDDAO sql = new DVDDAO(con);
        DVD c = new DVD();

        if(nome.equals("")){
            JOptionPane.showMessageDialog(null,"Nenhum Nome selecionado",
                    "Video Locadora",JOptionPane.WARNING_MESSAGE);
        }else{
            int b = JOptionPane.showConfirmDialog(null,"Deseja realmene excluir \n "
                    + "("+cod+") ("+nome+")","Video Locadora",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(b==0){
                int codigo = Integer.parseInt(cod);
                c.setCodigo(codigo);
                c.setSituacao(nome);
                sql.ExcluirDVD(c);
                Conexao.FecharConexao(con);
                dispose();
            }

        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

