    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String co = codd.getText();
       int codigo = Integer.parseInt(codig.getText()); 
       String nome = no.getText();
       String login = userr.getText();
       String senha = pass.getText();
       
       if(nome.equalsIgnoreCase("")||login.equalsIgnoreCase("")||senha.equalsIgnoreCase("")){
           JOptionPane.showMessageDialog(null,"Todos os Campos Precisam ser Preenchidos","Erro",JOptionPane.WARNING_MESSAGE);
       
       }else{
           Connection con = Conexao.AbrirConexao();
           FuncionarioDAO sql = new FuncionarioDAO(con);
           int c = Integer.parseInt(co);
           Funcionario f = new Funcionario();
         
           f.setCod(c);
           f.setNome(nome);
           f.setLogin(login);
           f.setSenha(senha);
           sql.Alterar_Funcionario(f);
           
           Conexao.FecharConexao(con);
            codig.setText("");
            no.setText("");
            userr.setText("");
            pass.setText("");
            
            JOptionPane.showMessageDialog(null,"Atualizado Com Sucesso","Mensagem",JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigo = codig.getText();
        int cod = 0;
        Connection con = Conexao.AbrirConexao();
        FuncionarioDAO sql = new FuncionarioDAO(con);

                
        if(codigo.equals("")){
            JOptionPane.showMessageDialog(null,"Digite um CÃ³digo Para Atualizar","Erro",JOptionPane.WARNING_MESSAGE);
            no.setText("");
            userr.setText("");
            pass.setText("");
        }else{
        cod = Integer.parseInt(codigo);
        if(sql.Testar_funcionario(cod)==false){
              JOptionPane.showMessageDialog(null,"Funcionario nÃ£o encontrado","Erro",JOptionPane.ERROR_MESSAGE);
             no.setText("");
              userr.setText("");
              pass.setText("");
              Conexao.FecharConexao(con);
        }else{
       no.setText("");
        userr.setText("");
        pass.setText("");
       
        InserirDados(cod);
        }
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

